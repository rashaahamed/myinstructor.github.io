// Parsing data

import java.util.*;

public class Parse {
  public static void main(String[] main) {
    System.out.println(new Scanner("16").nextByte());
    System.out.println(new Scanner("42").nextInt());
    System.out.println(new Scanner("2234657899").nextLong());
    System.out.println(new Scanner("5.9").nextFloat());
    System.out.println(new Scanner("746.22").nextDouble());
    System.out.println(new Scanner("true").nextBoolean());
  }
}
