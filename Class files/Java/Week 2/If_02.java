// The if-else statement

public class If_02 {
  public static void main(String[] main) {
    boolean valid = true;

    if (valid = false) {
      System.out.println("Valid");
    } else {
      System.out.println("Invalid");
    }

    boolean ready1 = true;
    boolean ready2 = false;

    if (ready1)
      System.out.println("Ready");

    if (ready1 && ready2) {
      System.out.println("We are both ready");
    }
  }
}
