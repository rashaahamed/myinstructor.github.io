// Primitive types

/*
 * Java has 8 primitive types. These data types represent the building
 * blocks for Java objects. Java is a strongly typed language
 */

public class Types {
  public static void main(String[] main) {
    // Local variables - they must be initialized
    boolean a = true;
    char b = 'b';
    byte c = 1;
    short d = 2;
    int c = 12345;
    long d = 56789875l;
    float e = 7.0f;
    double h = 8.0;
    // Primitives can't invoke methods
  }
}
