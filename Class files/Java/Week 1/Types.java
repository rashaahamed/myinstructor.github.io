// Primitive types

/*
 * Java has eight primitive data types. These data types represent the building
 * blocks for Java objects
 * Java is a strongly typed language
 */

public class Types {
  public static void main(String[] main) {
     // These are local variables - they must be initialized
     // We can't use primitives to call/invoke a method
     boolean a = true;
     char b = 'b';
     byte c = 3;
     short d = 4;
     int e = 2345;
     long f = 345679l;
     float g = 45.3f;
     double h = 789.34;
  }
}
