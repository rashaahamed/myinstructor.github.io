// Variables (primitves)

/*
 * Java has 8 primitive data types. These data types represent the building
 * block for Java objects. These types are called 'primitive' because you cannot
 * use them to create objects
 */

public class Variables {
  public static void main(String[] args) {
    boolean a = true;
    char b = 'b';
    byte c = 3;
    short d = 4;
    int e = 5;
    long f = 66666666l;
    float g = 7.0f;
    double h = 8.0;
  }
}
