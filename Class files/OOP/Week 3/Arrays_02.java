// Using arrays (for loop)

public class Arrays_02 {
  public static void main(String[] args) {
    // The length of the array is 6 (elements)
    // 0 - 5
    int[] a1 = {1, 2, 3, 4, 5, 6};

    // i -> iterator
    for (int i = 0; i < a1.length; i++) {
      System.out.print(a1[i] + " ");
    }
    System.out.println();
  }
}
