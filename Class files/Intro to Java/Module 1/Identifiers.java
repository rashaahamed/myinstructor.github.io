// Identifiers

/*
 *  An indentifier is a programmer-defined name that represents some element
 *  of a program
 *
 *  The name must begin with a: letter, '$', or '_'
 *  You cannot use the same name as a Java reserved (keywords) word
 *
 *  CamelCase (snake_case)
 */

public class Identifiers {
  public static void main(String[] args) {
   int num = 1;
   int __n = 2;
   int _33 = 3;
   int _$_ = 4;
   int $05 = 5;
   int Num = 6;  // Not a godd idea
  }
}
